// 単元IDから単元情報を取得
export function getUnitById(unitId) {
  for (const subject in unitsDatabase) {
    for (const grade in unitsDatabase[subject]) {
      const unit = unitsDatabase[subject][grade].find(u => u.id === unitId)
      if (unit) {
        return { ...unit, subject, grade }
      }
    }
  }
  return null
}

// 科目と学年から単元リストを取得
export function getUnitsBySubjectAndGrade(subject, grade) {
  return unitsDatabase[subject]?.[grade] || []
}

